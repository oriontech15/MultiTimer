import UIKit

class ColorPalette: UIColor
{
    static func pLightRedColor() -> UIColor
    {
        return UIColor(red: 0.933, green: 0.434, blue: 0.451, alpha: 1.00)
    }
    
    static func pRedColor() -> UIColor
    {
        return UIColor(red: 0.933, green: 0.165, blue: 0.255, alpha: 1.00)
    }
    
    static func pBlueColor() -> UIColor
    {
        return UIColor(red: 0.110, green: 0.812, blue: 0.996, alpha: 1.00)
    }
    
    static func pLightYellowColor() -> UIColor
    {
        return UIColor(red: 1.000, green: 0.991, blue: 0.646, alpha: 1.00)
    }
    
    static func pLightBlueColor() -> UIColor
    {
        return UIColor(red: 0.501, green: 0.996, blue: 0.994, alpha: 1.00)
    }
    
    static func pYellowColor() -> UIColor
    {
        return UIColor(red: 0.996, green: 0.906, blue: 0.333, alpha: 1.00)
    }
    
    static func pPinkColor() -> UIColor
    {
        return UIColor(red: 1.000, green: 0.468, blue: 0.808, alpha: 1.00)
    }
}
